def should_show_help(user_id: int, button_text: str) -> bool:
    """
    Check if help should be shown for a button.
    
    Args:
        user_id: User ID
        button_text: Button text
        
    Returns:
        True if help should be shown (only once per button)
    """
    # Check user preference
    help_key = f"button_help_shown_{button_text}"
    user_setting = db.get_user_setting(user_id)
    
    # If user setting doesn't exist, show help
    if user_setting is None:
        return True
    
    # Get preferences dictionary
    preferences = user_setting.get('preferences', {})
    
    # Check if help was already shown for this button
    help_shown = preferences.get(help_key)
    
    # If help was never shown, show it
    if help_shown is None:
        return True
    
    # If help was already shown (value is "true"), don't show again
    if help_shown == "true":
        return False
    
    # If user explicitly disabled help (value is "false"), don't show
    if help_shown == "false":
        return False
    
    # Default: show help if not set
    return True


def mark_help_shown(user_id: int, button_text: str, shown: bool = True):
    """
    Mark help as shown or hidden for a button.
    
    Args:
        user_id: User ID
        button_text: Button text
        shown: Whether help was shown (True) or hidden (False)
    """
    help_key = f"button_help_shown_{button_text}"
    db.set_user_preference(user_id, help_key, "true" if shown else "false")


def reset_all_help(user_id: int):
    """
    Reset all button help preferences (show all help again).
    
    Args:
        user_id: User ID
    """
    for button_text in BUTTON_HELP.keys():
        help_key = f"button_help_shown_{button_text}"
        db.set_user_preference(user_id, help_key, None)

